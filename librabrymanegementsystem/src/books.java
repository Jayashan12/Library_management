    private void search1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search1ActionPerformed
        // TODO add your handling code here:
        
        try{
        
            
                    stmt = con.createStatement();
            
            int sBookid = Integer.parseInt(bookid.getText());
            
            String query = "SELECT * FROM books";
            
            rs = stmt.executeQuery(query);
            
            while(rs.next())
            {
                if(sBookid == rs.getInt("bookid"))
                {

title.setText(rs.getString("title"));
authur.setText(rs.getString("authur"));
genre.setSelectedItem(rs.getString("genre"));
year.setText(String.format("%s", rs.getInt("year")));
quantity.setText(String.format(0 +  "%s", rs.getInt("quantity")));

                }
            }
        }
        catch (Exception e)
        {
        System.out.println(e);
        }
    }//GEN-LAST:event_search1ActionPerformed


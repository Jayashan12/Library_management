    private void booksubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_booksubmitActionPerformed
        // TODO add your handling code here:
        try
        {
        stmt = con.createStatement();
        
         int sBookid=Integer.parseInt(bookid.getText());
         String sTitle =title.getText();
         String sAuthur =authur.getText();
         String sGenre = (String) genre.getSelectedItem();
         int sYear = Integer.parseInt(year.getText());
         int sQuantity = Integer.parseInt(quantity.getText());
                  
         
        String query ="INSERT INTO books (bookid,title,authur,genre,year,quantity) VALUES (?,?,?,?,?,?)";
        PreparedStatement preparedStmt=con.prepareStatement(query);
        
        preparedStmt.setInt(1,sBookid);
        preparedStmt.setString(2,sTitle);
        preparedStmt.setString(3,sAuthur);
        preparedStmt.setString(4,sGenre);
        preparedStmt.setInt(5,sYear);
        preparedStmt.setInt(6,sQuantity);
        
        preparedStmt.execute();
        JOptionPane.showMessageDialog(null,"ADDED");
        }
        catch (Exception e)
                {
                System.out.println(e);
                }
    }//GEN-LAST:event_booksubmitActionPerformed

